import UIKit

class ColorSetup {
    static func white() -> UIColor {
        return hexColor(value: "E4E2E9")
    }
    
    static func green() -> UIColor {
        return hexColor(value: "71D9BF")
    }
    
    static func orange() -> UIColor {
        return hexColor(value: "FC5521")
    }
    
    static func purpleLight() -> UIColor {
        return hexColor(value: "AF92D0")
    }
    
    static func purpleDark() -> UIColor {
        return hexColor(value: "8864B3")
    }
    
    static func placeholder() -> UIColor {
        return hexColor(value: "636267")
    }
    
    static func black() -> UIColor {
        return hexColor(value: "19181D")
    }
    
}
